package com.test.java;


public class EX03_Variable {
	public static void main(String[] args) {
		byte kor=100;
		System.out.println(kor);
	}
	}




//메모리 번지수 -> 별칭(이름) -> kor -> 공간 자체를 변수라고 부름



/*

변수, variable : 개발자가 명령어를 사용해서 메모리를 할당받은 공간
목적 : 원하는 데이터를 읽거나 쓰기 위한 공간


1. 생성하기
	- 자료형 변수명;
	byte kor;

	

2. 초기화하기
	- 변수 = 값;
	kor = 100;

	

3. 사용하기
	- System.out.println(kor);

	

*/